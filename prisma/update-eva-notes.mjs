async function main() {
  console.log('Adding new EVA hardcore notes...\n')

  let addedCount = 0

  for (const [questTitle, notes] of Object.entries(NEW_NOTES_BY_QUEST)) {
    // 查找对应 Quest
    const task = await prisma.task.findFirst({
      where: { title: questTitle },
      include: { notes: true },
    })

    if (!task) {
      console.log(`  ⚠ Quest not found: "${questTitle}" — skipping`)
      continue
    }

    const existingTitles = new Set(task.notes.map((n) => n.title))

    for (const note of notes) {
      if (existingTitles.has(note.title)) {
        console.log(`  ⏭ Already exists: "${note.title}"`)
        continue
      }

      await prisma.note.create({
        data: {
          taskId: task.id,
          title: note.title,
          content: note.content,
        },
      })
      addedCount++
      console.log(`  + Added: "${note.title}" → ${questTitle}`)
    }
  }

  console.log(`\nDone! Added ${addedCount} new notes.`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
